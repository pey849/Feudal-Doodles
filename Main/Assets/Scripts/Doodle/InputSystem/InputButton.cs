namespace Doodle.InputSystem
{
    public enum InputButton
    {
        /// <summary>
        /// Button that will trigger the player to jump. Ex: "A"
        /// </summary>
        Jump,

        /// <summary>
        /// Button that will trigger the player to sprint. Ex: "X"
        /// </summary>
        Sprint,

        /// <summary>
        /// Button that will trigger the player to grab/interact. Ex: "B"
        /// </summary>
        Grab,

        /// <summary>
        /// A "button" that determines if the touch-screen is being actively touched.
        /// </summary>
        Touch
    }
}
